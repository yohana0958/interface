package POO;

public class carro {

	String modelo;
	String cor;
	String marca;
	int quantiPassageiro;
	
	public String getmodelo () {
		return modelo;
	}
	public void setmodelo (String modelo) {
		this.modelo = modelo;
	}
	public String getcor () {
		return cor;
	}
	public void setcor (String cor) {
		this.cor = cor;
	}
	public String getmarca () {
		return marca ;
	}
	public void setmarca (String marca) {
		this.marca = marca;
	}
	public int getquantiPassageiro () {
		return quantiPassageiro;
	}
	public void setquantiPassageiro (int quantiPassageiro) {
		this.quantiPassageiro = quantiPassageiro;
	}
		
}
	